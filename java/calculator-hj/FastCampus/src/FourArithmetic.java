public class FourArithmetic {
    public static void main(String[] args) {
        int numFirst, numSecond;
        numFirst = 30;
        numSecond = 20;
        System.out.println(numFirst + " + " + numSecond + " = " + (numFirst + numSecond));
        System.out.println(numFirst + " - " + numSecond + " = " + (numFirst - numSecond));
        System.out.println(numFirst + " * " + numSecond + " = " + (numFirst * numSecond));
        System.out.println(numFirst + " / " + numSecond + " = " + (numFirst / numSecond));
        System.out.println(numFirst + " % " + numSecond + " = " + (numFirst % numSecond));
    }
}
